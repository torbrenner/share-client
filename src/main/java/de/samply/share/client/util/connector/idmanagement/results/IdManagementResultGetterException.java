package de.samply.share.client.util.connector.idmanagement.results;

public class IdManagementResultGetterException extends Exception {

  public IdManagementResultGetterException() {
  }

  public IdManagementResultGetterException(String message) {
    super(message);
  }

  public IdManagementResultGetterException(String message, Throwable cause) {
    super(message, cause);
  }

  public IdManagementResultGetterException(Throwable cause) {
    super(cause);
  }

  public IdManagementResultGetterException(String message, Throwable cause,
      boolean enableSuppression, boolean writableStackTrace) {
    super(message, cause, enableSuppression, writableStackTrace);
  }

}
