package de.samply.share.client.util.connector.idmanagement.results;

public class ProjectFactoryException extends Exception {

  public ProjectFactoryException() {
  }

  public ProjectFactoryException(String message) {
    super(message);
  }

  public ProjectFactoryException(String message, Throwable cause) {
    super(message, cause);
  }

  public ProjectFactoryException(Throwable cause) {
    super(cause);
  }

  public ProjectFactoryException(String message, Throwable cause, boolean enableSuppression,
      boolean writableStackTrace) {
    super(message, cause, enableSuppression, writableStackTrace);
  }

}
