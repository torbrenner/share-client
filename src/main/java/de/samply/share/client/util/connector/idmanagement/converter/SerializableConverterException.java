package de.samply.share.client.util.connector.idmanagement.converter;

public class SerializableConverterException extends Exception {

  public SerializableConverterException() {
  }

  public SerializableConverterException(String message) {
    super(message);
  }

  public SerializableConverterException(String message, Throwable cause) {
    super(message, cause);
  }

  public SerializableConverterException(Throwable cause) {
    super(cause);
  }

}
