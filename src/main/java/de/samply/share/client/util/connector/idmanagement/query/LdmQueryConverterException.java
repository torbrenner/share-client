package de.samply.share.client.util.connector.idmanagement.query;

public class LdmQueryConverterException extends Exception {

  public LdmQueryConverterException() {
  }

  public LdmQueryConverterException(String message) {
    super(message);
  }

  public LdmQueryConverterException(String message, Throwable cause) {
    super(message, cause);
  }

  public LdmQueryConverterException(Throwable cause) {
    super(cause);
  }

  public LdmQueryConverterException(String message, Throwable cause, boolean enableSuppression,
      boolean writableStackTrace) {
    super(message, cause, enableSuppression, writableStackTrace);
  }
}
